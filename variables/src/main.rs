fn main() {
    let unsigned: u8 = 10;

    let signed: i8 = 12;

    let float: f32 = 11.2;

    println!("unsigned: {} signed: {} float: {}", unsigned, signed, float);
    
    let letter = "c";
    let emoji = "\u{1F600}";

    println!("letter: {} emoji: {}", letter, emoji);

    let is_true: bool = true;

    println!("is_true: {}", is_true);
}
